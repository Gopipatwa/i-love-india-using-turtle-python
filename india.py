import turtle
win = turtle.Screen()
win.bgcolor("black")


r = turtle.Turtle()
r.penup()
r.goto(0,-150)
r.pendown()
r.fillcolor("white")
r.begin_fill()
r.pensize(10)
r.left(25)
r.pencolor("green")
r.circle(300,60)
r.pencolor("red")
r.circle(90,180)
r.right(170)
r.pencolor("red")
r.circle(90,180)
r.pencolor("orange")
r.circle(300,62)
r.end_fill()

r.penup()
r.pencolor("blue")
r.pensize(2)
r.goto(-60,20)
r.pendown()
for i in range(24):
	r.forward(30)
	r.left(15)
	r.forward(80)
	r.left(180)
r.penup()
r.goto(-70,20)
r.pendown()
r.right(85)
r.circle(65)

r.penup()
r.goto(-270,-100)
r.pencolor("orange")
r.pendown()
r.write("\u0049",font=("bold",120))
r.penup()
r.goto(200,-100)
r.pendown()
r.pencolor("green")
r.write("\u0049\u004E\u0044\u0049\u0041",font=("bold",120))

r.penup()
r.goto(-100,300)
r.pendown()
r.pencolor("yellow")
r.write("@__silentcoder__",font=("bold",20))
r.hideturtle()
win.exitonclick()